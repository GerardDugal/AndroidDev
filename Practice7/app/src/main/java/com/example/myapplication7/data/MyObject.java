package com.example.myapplication7.data;

public class MyObject {
    private final int number;
    private final String string;

    public MyObject(int number, String string)
    {
        this.number = number;
        this.string = string;
    }

    public int getNumber() {
        return number;
    }

    public String getString() {
        return string;
    }
}
